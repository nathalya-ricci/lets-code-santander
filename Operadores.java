public class Operadores {

  public static void main(String[] args) {

    int a;
    int b = 2;
    a = 3;
    int soma = a + b;
    int sub = a - b;
    int mult = a * b;
    float div = (float) a / b;

    System.out.println(soma);
    System.out.println(sub);
    System.out.println(mult);
    System.out.println(div);
  }
}
